import {
  Initialized as InitializedEvent,
  NewElection as NewElectionEvent,
  RoleAdminChanged as RoleAdminChangedEvent,
  RoleGranted as RoleGrantedEvent,
  RoleRevoked as RoleRevokedEvent
} from "../generated/ElectionFactory/ElectionFactory"
import {
  Initialized,
  NewElection,
  RoleAdminChanged,
  RoleGranted,
  RoleRevoked
} from "../generated/schema"

export function handleInitialized(event: InitializedEvent): void {
  let entity = new Initialized(
    event.transaction.hash.concatI32(event.logIndex.toI32())
  )
  entity.version = event.params.version

  entity.blockNumber = event.block.number
  entity.blockTimestamp = event.block.timestamp
  entity.transactionHash = event.transaction.hash

  entity.save()
}

export function handleNewElection(event: NewElectionEvent): void {
  let entity = new NewElection(
    event.transaction.hash.concatI32(event.logIndex.toI32())
  )
  entity.electionId = event.params.id
  entity.electionAddr = event.params.election
  entity.owner = event.params.owner
  entity.totalVotes = 0;
  entity.blockNumber = event.block.number
  entity.blockTimestamp = event.block.timestamp
  entity.transactionHash = event.transaction.hash

  entity.save()
}

